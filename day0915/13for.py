# for반복문 변수  in  range(숫자)

for x in range(10): # 0부터시작 10-1끝
  print(x , end = ' ')

print()
for y in range(1, 11, 1): # 1시작  11-1끝 , 1씩증가는 생략가능 
  print(y , end = ' ')

print()
for z in range(0,10,2): # 0시작, 10-1끝, 2씩증가 
  print(z , end = ' ')

print()
for x in range(10,0,-1): # 10부터시작 0끝 -1씩 감소 
  print(x , end = ' ')


